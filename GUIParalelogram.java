    private void vectorListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vectorListaActionPerformed
        // TODO add your handling code here:
        latura1Ecran.setText("");        //aici cele 4 casute ca sa se stearga datele precedente,sa fie gol
        latura2Ecran.setText("");
        inaltimeEcran.setText("");
        ecranAfisarePerimetru.setText("");
        ecranAfisareArie.setText("");
        textAfisareTot.setEnabled(true);

        Paralelogram p1 = new Paralelogram();
        Paralelogram p2 = new Paralelogram(3.0, 4.0, 5.0, 70);
        Paralelogram p3 = new Paralelogram(2, 7, 5, 120);
        Paralelogram p4 = new Paralelogram(6, 8, 5, 85);

        ArrayList<Paralelogram> vectPat = new ArrayList<Paralelogram>();  //am creat un vector care nu are o limita de elemente impusa
        vectPat.add(p1);
        vectPat.add(p2);
        vectPat.add(p3);
        vectPat.add(p4);

        String optiune = String.valueOf(vectorLista.getSelectedItem());  //pentru a alege din valorile prestabilite

        switch (optiune) {
            case "--------------------": {
                textAfisareTot.setText("");
                break;
            }
            case "Paralelogram 1": {
                textAfisareTot.setText(vectPat.get(0).toString());
                break;
            }
            case "Paralelogram 2": {
                textAfisareTot.setText(vectPat.get(1).toString());
                break;
            }
            case "Paralelogram 3": {
                textAfisareTot.setText(vectPat.get(2).toString());
                break;
            }
            case "Paralelogram 4": {
                textAfisareTot.setText(vectPat.get(3).toString());
                break;
            }

        }
    }//GEN-LAST:event_vectorListaActionPerformed

