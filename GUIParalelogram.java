    private void latura1EcranFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_latura1EcranFocusLost
        // TODO add your handling code here:
        mesajLatura1 = latura1Ecran.getText();    //citire mesaj de la casuta latura1
        try {
            Latura1 = Double.parseDouble(mesajLatura1); //daca este numar sau nu
            if (Latura1 < 0) {

                latura1Ecran.setText("EROARE! Negativ");
                System.out.println("EROARE! Negativ");

            }
            if (Latura1 > 100) {
                latura1Ecran.setText("EROARE! Depasit");
            }
        } catch (NumberFormatException e) {
            if (mesajLatura1.isEmpty()) {
                latura1Ecran.setText("");
            } else {
                latura1Ecran.setText("EROARE!Nan");
                System.out.println("EROARE!Nan");
            }
        }
    }//GEN-LAST:event_latura1EcranFocusLost

