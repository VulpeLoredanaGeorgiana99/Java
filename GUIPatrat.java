    private void butonPerimetruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butonPerimetruActionPerformed
        // TODO add your handling code here:
        mesajLatura = LaturaEcran.getText();   //se extrag stringurile citite in casuta alba
        try {
            latura = Double.parseDouble(mesajLatura);     //converteste din string in double
            if (latura > 0) {
                if (textAfisareTot.isEnabled() == false) {  //daca este dezactivata casuta de "mai multe"
                    textAfisareTot.setEnabled(true);   //se activeaza din nou
                }
                patrat.setLatura(latura);

                double perimetru = patrat.calculPerimetru();
                patrat.calculDiagonala();

                System.out.println("Perimetrul este " + perimetru);
                ecranAfisarePerimetru.setText(Double.toString(perimetru));
            
            }

        } catch (Exception e) { //alte erori,mai ales cele in care nu estenumar,ci este altceva cee ce se citeste de la diemnsiuni

            ecranAfisarePerimetru.setText("EROARE! Dim incorecta");
            System.out.println("EROARE! Dimensiune incorecta");
        }

    }//GEN-LAST:event_butonPerimetruActionPerformed

