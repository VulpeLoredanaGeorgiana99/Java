    private void butonPerimetruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butonPerimetruActionPerformed
        // TODO add your handling code here:
        mesajLatura1 = latura1Ecran.getText();
        mesajLatura2 = latura2Ecran.getText();
        mesajLatura3 = latura3Ecran.getText();
        mesajLatura4 = latura4Ecran.getText();

        try {                                             //se verifica daca sunt numere sau alte caractere
            latura1 = Double.parseDouble(mesajLatura1);
            latura2 = Double.parseDouble(mesajLatura2);
            latura3 = Double.parseDouble(mesajLatura3);
            latura4 = Double.parseDouble(mesajLatura4);

            if (latura1 > 0 && latura2 > 0 && latura3 > 0 && latura4 > 0) {

                patrulater.setLatura1(latura1);
                patrulater.setLatura2(latura2);
                patrulater.setLatura3(latura3);
                patrulater.setLatura4(latura4);

                double perim = patrulater.calculPerimetru();

                System.out.println("Perimetrul este " + perim);
                ecranPerimetru.setText(Double.toString(perim));
            }
        } catch (Exception e) { //alte erori,mai ales cele in care nu estenumar,ci este altceva cee ce se citeste de la diemnsiuni

            ecranPerimetru.setText("EROARE!Dim incorecte");
            System.out.println("EROARE!dimensiuni incorecte");
        }

    }//GEN-LAST:event_butonPerimetruActionPerformed

