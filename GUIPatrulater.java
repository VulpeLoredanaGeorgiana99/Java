        switch (optiune) {
            case "---------------------": {
                ecranAfisareTot.setText("");
                break;
            }
            case "Patrulater 1": {
                ecranAfisareTot.setText(vectPatr.get(0).toString());
                break;
            }
            case "Patrulater 2": {
                ecranAfisareTot.setText(vectPatr.get(1).toString());
                break;
            }
            case "Patrulater 3": {
                ecranAfisareTot.setText(vectPatr.get(2).toString());
                break;
            }
            case "Patrulater 4": {
                ecranAfisareTot.setText(vectPatr.get(3).toString());
                break;
            }
        }
