    private void butonMaiMulteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butonMaiMulteActionPerformed
        // TODO add your handling code here:
        mesajBazaMare = bazaMareEcran.getText();
        mesajBazaMica = bazaMicaEcran.getText();
        mesajLatura3 = latura3Ecran.getText();
        mesajLatura4 = latura4Ecran.getText();
        mesajInaltime = inaltimeEcran.getText();
        try {
            bazaMica = Double.parseDouble(mesajBazaMica);              //daca laturile citite sunt numere 
            bazaMare = Double.parseDouble(mesajBazaMare);
            Latura3 = Double.parseDouble(mesajLatura3);              //daca laturile citite sunt numere 
            Latura4 = Double.parseDouble(mesajLatura4);
            inaltime = Double.parseDouble(mesajInaltime);

            if (bazaMica >= 0 && bazaMare >= 0 && Latura3 >= 0 && Latura4 >= 0 && inaltime >= 0) {

                textAfisareTot.setText(trapez.toString());
            } else if (bazaMica < 0 || bazaMare < 0 || Latura3 < 0 || Latura4 < 0 || inaltime < 0) {   //daca sunt negative laturile
                textAfisareTot.setText("");
                textAfisareTot.setEnabled(false);
            }

        } catch (Exception e) {   //daca nu sunt numere 
            textAfisareTot.setText("");   //astfel se goleste casuta cu "MAI MULTE"
            textAfisareTot.setEnabled(false);   //ea nu va mai putea fi editabila,deci nu va mai aparea nimic,dezactivata

        }
    }//GEN-LAST:event_butonMaiMulteActionPerformed

