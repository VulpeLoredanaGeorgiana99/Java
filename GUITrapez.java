    private void butonPerimetruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butonPerimetruActionPerformed
        // TODO add your handling code here:
        mesajBazaMare = bazaMareEcran.getText();
        mesajBazaMica = bazaMicaEcran.getText();
        mesajLatura3 = latura3Ecran.getText();
        mesajLatura4 = latura4Ecran.getText();
        mesajInaltime = inaltimeEcran.getText();

        try {                                             //se verifica daca sunt numere sau alte caractere
            bazaMica = Double.parseDouble(mesajBazaMica);
            bazaMare = Double.parseDouble(mesajBazaMare);
            Latura3 = Double.parseDouble(mesajLatura3);
            Latura4 = Double.parseDouble(mesajLatura4);
            inaltime = Double.parseDouble(mesajInaltime);
            if (bazaMica >= 0 && bazaMare >= 0 && Latura3 >= 0 && Latura4 >= 0 && inaltime >= 0) {
                if (textAfisareTot.isEnabled() == false) {  //daca este dezactivata casuta de "mai multe"
                    textAfisareTot.setEnabled(true);   //se activeaza din nou
                }
                trapez.setLatura1(bazaMica);
                trapez.setLatura2(bazaMare);
                trapez.setLatura3(Latura3);
                trapez.setLatura4(Latura4);
                trapez.setInaltime(inaltime);

                double perim = trapez.calculPerimetru();
                System.out.println("Perimetrul este: " + perim);
                ecranAfisarePerimetru.setText(Double.toString(perim));
            }
        } catch (Exception e) { //alte erori,mai ales cele in care nu estenumar,ci este altceva cee ce se citeste de la diemnsiuni

            ecranAfisarePerimetru.setText("EROARE!Dim incorecte!");
            System.out.println("EROARE!Dim incorecte!");
        }
    }//GEN-LAST:event_butonPerimetruActionPerformed

